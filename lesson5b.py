# Example 2 

def addiction (Num1,Num2) :
    Ans=Num1+Num2
    print("sum",Ans)
# called out the function 
addiction(40,60)

# NB: a function is useable block of code that perform specific task

addiction (700,900)
addiction (1500,400)
addiction (-160,-40)

